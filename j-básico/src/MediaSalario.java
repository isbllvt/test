package exercícios;

public class MediaSalario {

    public static void main(String[] args) {
       //declarando variáveis
        double media, n1, n2, n3, n4;
        //armazenando valores para calcular a média
        n1 = 1200.0;
        n2 = 1400.0;
        n3 = 3000.2;
        n4 = 2000.1;
//simples calculo da média salarial e print
        media = (n1 + n2 + n3 + n4) / 4;
        System.out.println("A média salarial dos funcinérios é de: " + media);
    }
}
